package entities;

public class ParametreEtbalissement {
	private String url;
	private Etablissement etab;
	private Parametre param;
	
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Etablissement getEtab() {
		return etab;
	}
	public void setEtab(Etablissement etab) {
		this.etab = etab;
	}
	public Parametre getParam() {
		return param;
	}
	public void setParam(Parametre param) {
		this.param = param;
	}
	

}
